# 年上起月（五虎遁元）
def get_month_stem(year_stem, month_zhi_index):
    """根据年干和月支序号(0=寅月)返回月干"""
    starts = {"甲": "丙", "己": "丙", "乙": "戊", "庚": "戊",
              "丙": "庚", "辛": "庚", "丁": "壬", "壬": "壬",
              "戊": "甲", "癸": "甲"}
    start_gan = starts[year_stem]
    start_idx = TIAN_GAN.index(start_gan)
    return TIAN_GAN[(start_idx + month_zhi_index) % 10]

