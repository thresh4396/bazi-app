# 以日干为中心，其他天干与日干的关系
def get_shi_shen(ri_gan, other_gan):
    """返回 other_gan 相对于 ri_gan 的十神"""
    ri_wx = TIAN_GAN_WUXING[ri_gan]
    other_wx = TIAN_GAN_WUXING[other_gan]
    ri_yy = TIAN_GAN_YINYANG[ri_gan]
    other_yy = TIAN_GAN_YINYANG[other_gan]
    same_yy = (ri_yy == other_yy)

    # 相生关系
    sheng = {
        "木": "火", "火": "土", "土": "金", "金": "水", "水": "木"
    }
    # 相克关系
    ke = {
        "木": "土", "土": "水", "水": "火", "火": "金", "金": "木"
    }

    if ri_wx == other_wx:  # 同我
        return "比肩" if same_yy else "劫财"
    elif sheng.get(other_wx) == ri_wx:  # 生我
        return "偏印" if same_yy else "正印"
    elif sheng.get(ri_wx) == other_wx:  # 我生
        return "食神" if same_yy else "伤官"
    elif ke.get(other_wx) == ri_wx and same_yy:  # 克我且同阴阳
        return "七杀"
    elif ke.get(other_wx) == ri_wx and not same_yy:  # 克我且异阴阳
        return "正官"
    elif ke.get(ri_wx) == other_wx:  # 我克
        return "偏财" if same_yy else "正财"
    return "未知"

